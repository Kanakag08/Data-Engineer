# set and set method

# set add method
fruits = {"apple", "banana", "cherry"}
fruits.add("orange")
print(fruits)

# clear method
fruits = {"apple", "banana", "cherry"}
fruits.clear()
print(fruits)

# copy method
fruits = {"apple", "banana", "cherry"}
x = fruits.copy()
print(x)

# pop method
fruits = {"apple", "banana", "cherry"}
fruits.pop()
print(fruits)


# remove method
fruits = {"apple", "banana", "cherry"}
fruits.remove("banana")
print(fruits)


#update method
x = {"apple", "banana", "cherry"}
y = {"google", "microsoft", "apple"}
x.update(y)
print(x)